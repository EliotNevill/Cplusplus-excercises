const int gridsize = 80;


int is_alive(int x, int y, bool cells[gridsize][gridsize]){
	if (x >= 0 && x < gridsize && y >= 0 && y < gridsize){
		return cells[x][y]?1:0;
	}else{
		return 0;
	}
}


int get_alive_neighbours(int x, int y, bool cells[gridsize][gridsize]){
	int alive_neighbours = 0;
	alive_neighbours += is_alive(x-1 , y-1, cells);
	alive_neighbours += is_alive(x , y-1, cells);
	alive_neighbours += is_alive(x+1 , y-1, cells);
	alive_neighbours += is_alive(x-1 , y, cells);
	alive_neighbours += is_alive(x+1 , y, cells);
	alive_neighbours += is_alive(x-1 , y+1, cells);
	alive_neighbours += is_alive(x , y+1, cells);
	alive_neighbours += is_alive(x+1 , y+1, cells);
	return alive_neighbours;
}
void iterate(bool cells[gridsize][gridsize] , bool newcells[gridsize][gridsize]){
	for (int x = 0; x < gridsize; x++){
		for(int y = 0; y < gridsize; y++){
			int alive_neighbours = get_alive_neighbours(x,y,cells);
			if (cells[x][y] && (alive_neighbours <2 ||alive_neighbours >3 )){
				newcells[x][y] = false;
			}else if(!cells[x][y] && alive_neighbours == 3){
				newcells[x][y] = true;
			}else{
				newcells[x][y] = cells[x][y];
			}


		}
	}

}
